<?php

namespace AppBundle\Entity;


class CharacterSheetTemplate
{

    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var integer
     */
    private $version;

    /**
     * @var \AppBundle\Entity\RolGame
     */
    private $rol_game;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return CharacterSheetTemplate
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set version
     *
     * @param integer $version
     *
     * @return CharacterSheetTemplate
     */
    public function setVersion($version)
    {
        $this->version = $version;

        return $this;
    }

    /**
     * Get version
     *
     * @return integer
     */
    public function getVersion()
    {
        return $this->version;
    }

    /**
     * Set rolGame
     *
     * @param \AppBundle\Entity\RolGame $rolGame
     *
     * @return CharacterSheetTemplate
     */
    public function setRolGame(\AppBundle\Entity\RolGame $rolGame = null)
    {
        $this->rol_game = $rolGame;

        return $this;
    }

    /**
     * Get rolGame
     *
     * @return \AppBundle\Entity\RolGame
     */
    public function getRolGame()
    {
        return $this->rol_game;
    }
}
